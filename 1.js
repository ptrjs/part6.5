let i = 0

console.log("Loop 1 - Maju")
while(i<100){
    console.log(i)
    i++
}

console.log("-----------------------------")

let j = 100
console.log("Loop 2 - Mundur")
while(j>=0){
    console.log(j)
    j--
    
}

console.log("-----------------------------")